type k8sClient struct {
	*kubernetes.Clientset
	namespace    string
	projectID    string
	projectToken string
	cloudBaseURL string
}

func (client *k8sClient) ensureOwnNamespace(ctx context.Context) error {
	exists, err := client.ownNamespaceExists(ctx)
	if err != nil {
		return err
	}

	if exists {
		return nil
	}

	_, err = client.createOwnNamespace(ctx)
	return err
}

func (client *k8sClient) ownNamespaceExists(ctx context.Context) (bool, error) {
	_, err := client.CoreV1().Namespaces().Get(ctx, client.namespace, metav1.GetOptions{})
	if k8serrors.IsNotFound(err) {
		return false, nil
	}

	if err != nil {
		return false, err
	}

	return true, nil
}

func (client *k8sClient) createOwnNamespace(ctx context.Context) (*apiv1.Namespace, error) {
	return client.CoreV1().Namespaces().Create(ctx, &apiv1.Namespace{
		ObjectMeta: metav1.ObjectMeta{
			Name: client.namespace,
		},
	}, metav1.CreateOptions{})
}

func (client *k8sClient) createClusterRole(ctx context.Context, aggregatorName string) (*rbacv1.ClusterRole, error) {
	return client.RbacV1().ClusterRoles().Create(ctx, &rbacv1.ClusterRole{
		ObjectMeta: metav1.ObjectMeta{
			Name: aggregatorName + "-cluster-role",
			Labels: map[string]string{
				"calyptia_project_id":      client.projectID,
				"calyptia_aggregator_name": aggregatorName,
			},
		},
		Rules: []rbacv1.PolicyRule{
			{
				APIGroups: []string{"", "apps"},
				Resources: []string{
					"namespaces",
					"deployments",
					"replicasets",
					"pods",
					"services",
					"configmaps",
					"deployments/scale",
					"secrets",
				},
				Verbs: []string{
					"get",
					"list",
					"create",
					"delete",
					"patch",
					"update",
					"watch",
					"deletecollection",
				},
			},
		},
	}, metav1.CreateOptions{})
}

func (client *k8sClient) createServiceAccount(ctx context.Context, aggregatorName string) (*apiv1.ServiceAccount, error) {
	return client.CoreV1().ServiceAccounts(client.namespace).Create(ctx, &apiv1.ServiceAccount{
		ObjectMeta: metav1.ObjectMeta{
			Name: aggregatorName + "-service-account",
			Labels: map[string]string{
				"calyptia_project_id":      client.projectID,
				"calyptia_aggregator_name": aggregatorName,
			},
		},
	}, metav1.CreateOptions{})
}

func (client *k8sClient) createClusterRoleBinding(ctx context.Context, aggregatorName string) (*rbacv1.ClusterRoleBinding, error) {
	return client.RbacV1().ClusterRoleBindings().Create(ctx, &rbacv1.ClusterRoleBinding{
		ObjectMeta: metav1.ObjectMeta{
			Name: aggregatorName + "-cluster-role-binding",
			Labels: map[string]string{
				"calyptia_project_id":      client.projectID,
				"calyptia_aggregator_name": aggregatorName,
			},
		},
		RoleRef: rbacv1.RoleRef{
			APIGroup: "rbac.authorization.k8s.io",
			Kind:     "ClusterRole",
			Name:     aggregatorName + "-cluster-role",
		},
		Subjects: []rbacv1.Subject{
			{
				Kind:      "ServiceAccount",
				Name:      aggregatorName + "-service-account",
				Namespace: client.namespace,
			},
		},
	}, metav1.CreateOptions{})
}

func (client *k8sClient) createDeployment(ctx context.Context, aggregatorName string) (*appsv1.Deployment, error) {
	return client.AppsV1().Deployments(client.namespace).Create(ctx, &appsv1.Deployment{
		ObjectMeta: metav1.ObjectMeta{
			Name: aggregatorName + "-deployment",
			Labels: map[string]string{
				"calyptia_project_id":      client.projectID,
				"calyptia_aggregator_name": aggregatorName,
			},
		},
		Spec: appsv1.DeploymentSpec{
			Replicas: ptr(int32(1)),
			Selector: &metav1.LabelSelector{
				MatchLabels: map[string]string{
					"calyptia_project_id":      client.projectID,
					"calyptia_aggregator_name": aggregatorName,
				},
			},
			Template: apiv1.PodTemplateSpec{
				ObjectMeta: metav1.ObjectMeta{
					Labels: map[string]string{
						"calyptia_project_id":      client.projectID,
						"calyptia_aggregator_name": aggregatorName,
					},
				},
				Spec: apiv1.PodSpec{
					ServiceAccountName:           aggregatorName + "-service-account",
					AutomountServiceAccountToken: ptr(true),
					Containers: []apiv1.Container{
						{
							Name:  aggregatorName,
							Image: "ghcr.io/calyptia/core",
							Args:  []string{"-debug=true"},
							Env: []apiv1.EnvVar{
								{
									Name:  "AGGREGATOR_NAME",
									Value: aggregatorName,
								},
								{
									Name:  "PROJECT_TOKEN",
									Value: client.projectToken,
								},
								{
									Name:  "AGGREGATOR_FLUENTBIT_CLOUD_URL",
									Value: client.cloudBaseURL,
								},
							},
						},
					},
				},
			},
		},
	}, metav1.CreateOptions{})
}

func (config *config) aggregatorExistsWithName(ctx context.Context, name string) (bool, error) {
	aa, err := config.cloud.Aggregators(ctx, config.projectID, types.AggregatorsParams{
		Name: &name,
		Last: ptr(uint64(1)),
	})
	if err != nil {
		return false, err
	}

	return len(aa.Items) != 0, nil
}

func ptr[T any](p T) *T {
	return &p
}

func generateAggregatorName() (string, error) {
	rng, err := codename.DefaultRNG()
	if err != nil {
		return "", err
	}

	return codename.Generate(rng, 4), nil
}
