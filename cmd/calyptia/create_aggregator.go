func newCmdCreateAggregatorOnK8s(config *config) *cobra.Command {
	var aggregatorName string

	loadingRules := clientcmd.NewDefaultClientConfigLoadingRules()
	configOverrides := &clientcmd.ConfigOverrides{}

	cmd := &cobra.Command{
		Use:     "kubernetes",
		Aliases: []string{"kube", "k8s"},
		Short:   "Setup a new core instance on Kubernetes",
		RunE: func(cmd *cobra.Command, args []string) error {
			kubeConfig := clientcmd.NewNonInteractiveDeferredLoadingClientConfig(loadingRules, configOverrides)
			kubeClientConfig, err := kubeConfig.ClientConfig()
			if err != nil {
				return err
			}

			clientset, err := kubernetes.NewForConfig(kubeClientConfig)
			if err != nil {
				return err
			}

			ctx := context.Background()

			if aggregatorName != "" {
				exists, err := config.aggregatorExistsWithName(ctx, aggregatorName)
				if err != nil {
					return err
				}

				if exists {
					return fmt.Errorf("aggregator name %q taken", aggregatorName)
				}
			} else {
				var err error
				aggregatorName, err = generateAggregatorName()
				if err != nil {
					return err
				}
			}

			namespace := configOverrides.Context.Namespace
			k8sClient := &k8sClient{
				Clientset:    clientset,
				namespace:    namespace,
				projectID:    config.projectID,
				projectToken: config.projectToken,
				cloudBaseURL: config.baseURL,
			}

			if err := k8sClient.ensureOwnNamespace(ctx); err != nil {
				return err
			}

			clusterRole, err := k8sClient.createClusterRole(ctx, aggregatorName)
			if err != nil {
				return err
			}

			fmt.Printf("cluster role: %q\n", clusterRole.Name)

			serviceAccount, err := k8sClient.createServiceAccount(ctx, aggregatorName)
			if err != nil {
				return err
			}

			fmt.Printf("service account: %q\n", serviceAccount.Name)

			binding, err := k8sClient.createClusterRoleBinding(ctx, aggregatorName)
			if err != nil {
				return err
			}

			fmt.Printf("cluster role binding: %q\n", binding.Name)

			deploy, err := k8sClient.createDeployment(ctx, aggregatorName)
			if err != nil {
				return err
			}

			fmt.Printf("deployment: %q\n", deploy.Name)

			return nil
		},
	}

	fs := cmd.Flags()
	fs.StringVar(&aggregatorName, "name", "", "Aggregator name (autogenerated if empty)")
	clientcmd.BindOverrideFlags(configOverrides, fs, clientcmd.RecommendedConfigOverrideFlags("kube-"))

	return cmd
}

